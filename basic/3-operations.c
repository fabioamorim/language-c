#include <stdio.h>
#include <stdlib.h>

int main()
{

    int a = 10, b = 5;

    printf("%i %i", a, b);
    printf("\n");

    // sum
    printf("Sum = %i", a+b);
    printf("\n");

    // subtraction
    printf("Sub = %i", a-b);
    printf("\n");

    // multiplication
    printf("Mult = %i", a*b);
    printf("\n");

    // division
    printf("Div = %i", a/b);

    printf("\n\n");
    system("pause");
    return 0;

}